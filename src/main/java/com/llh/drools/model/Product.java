package com.llh.drools.model;

/**
 *
 * @author lorenzolince
 */
public class Product {

    private String type;
    private int price;

    public Product() {
    }

    public Product(String type) {
        this.type = type;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

}
